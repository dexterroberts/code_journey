## if Statements

# Simple if Statements: The simplest kind of if statement has one test and one action:

# if conditional_test:
    # do something

# EXAMPLE 1#
age = 19
if age >= 18:
    print("You are old enough to vote!")
    print("Have you registered to vote yet?")
